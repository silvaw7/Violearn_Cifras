package modelo;

public class Cifras {

    private int id;
    
    private String titulo;
    
    private String nvlDificuldade;
    
    private String nomeCompositor;
    
    private String generoMusical;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public String getNvlDificuldade() {
        return nvlDificuldade;
    }

    public void setNvlDificuldade(String nvlDificuldade) {
        this.nvlDificuldade = nvlDificuldade;
    }

    public String getNomeCompositor() {
        return nomeCompositor;
    }

    public void setNomeCompositor(String nomeCompositor) {
        this.nomeCompositor = nomeCompositor;
    }

    public String getGeneroMusical() {
        return generoMusical;
    }

    public void setGeneroMusical(String generoMusical) {
        this.generoMusical = generoMusical;
    }
}

